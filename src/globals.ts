/**
 * Enumeration representing user roles.
 * @enum {number}
 */
export enum USER_ROLE {
    /**
     * Administrator role.
     * @type {number}
     */
    ADMIN = 0,

    /**
     * Regular user role.
     * @type {number}
     */
    USER = 1,

    /**
     * Owner role.
     * @type {number}
     */
    OWNER = 2
}